    <script>
      document.addEventListener('alpine:init', () => {
        Alpine.store('validationError', {
          errors: {},
          setErrors(errors) {
            this.errors = errors;
          },
          getError(field) {
            return this.errors[field] ? this.errors[field][0] : null;
          }
        });
      });
      
      function invoiceComponent($wire) {
        return {
          items: [{
            date: '',
            cost: 0,
            quantity: 0,
            price: 0,
            description: ''
          }],
          tax: 0,
          discount: 0,
          errors: {},
          init() {
            window.addEventListener('validationError', event => {
              Alpine.store('validationError').setErrors(event.detail[0].errors);
              this.errors = event.detail[0].errors;
            });

            window.addEventListener('resetForm', () => {
              this.reset();
            });
          },
           reset() {
            this.items = [{
              date: '',
              cost: 0,
              quantity: 0,
              price: 0,
              description: ''
            }];
            this.tax = 0;
            this.discount = 0;
            this.errors = {};
          },
          addItem() {
            this.items.push({
              date: '',
              cost: 0,
              quantity: 0,
              price: 0,
              description: ''
            });
          },
          removeItem(index) {
            this.items.splice(index, 1);
          },
          calculate(index) {
            let item = this.items[index];
            item.price = item.cost * item.quantity;
          },
          get subtotal() {
            return this.items.reduce((sum, i) => sum + i.price, 0);
          },
          get taxAmount() {
            return (this.subtotal * this.tax) / 100;
          },
          get discountAmount() {
            return (this.subtotal * this.discount) / 100;
          },
          get total() {
            return this.subtotal + this.taxAmount - this.discountAmount;
          },
          formatCurrency(amount) {
            return new Intl.NumberFormat('en-US', {
              style: 'currency',
              currency: 'USD'
            }).format(amount);
          },
          sendToLivewire() {
            $wire.set('items', this.items);
            $wire.set('tax', this.tax);
            $wire.set('discount', this.discount);
            $wire.set('total', this.total);
            $wire.saveInvoice(); // Livewire method
          },
          getError(field) {
            return this.errors[field] ? this.errors[field][0] : null;
          }
        };
      }
    </script>
